package com.android.suppliesmanager;

import java.util.List;

import androidx.lifecycle.LiveData;
import androidx.room.Dao;
import androidx.room.Delete;
import androidx.room.Insert;
import androidx.room.Query;
import androidx.room.Update;


@Dao
public interface ProductDao {

    @Insert
    void insert(Product product);

    @Update
    void update(Product product);

    @Delete
    void delete(Product product);

    @Query("SELECT * FROM product_table")
    LiveData<List<Product>> getAllProducts();

    @Query("SELECT * FROM product_table WHERE groupId == :groupId")
    List<Product> getAllProductsByGroup(int groupId);

    @Query("SELECT * FROM product_table WHERE barcode LIKE :userInput OR title LIKE :userInput")
    List<Product> getAllProductsByUserInput(String userInput);
}
